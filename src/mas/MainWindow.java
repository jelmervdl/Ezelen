    public MainWindow(Table table) {
        initComponents();
        initOverlay();
        
        this.table = table;
        this.setSize(1300, 900);
        
        windows = new ArrayList<AgentWindow>();
        
        for (int i = 0; i < 4; ++i) {
            AgentWindow window = new AgentWindow();
            windows.add(window);
            agentsPanel.add(window.getContentPane());
        }
        
        
    }

    public void setTable(Table table)
    {
        this.table = table;
        
        table.addListener(this);
        
        List<Agent> agents = table.getAgents();
        
        assert agents.size() <= windows.size() : "There are not enough windows for all these agents";
        
        for (int i = 0; i < agents.size(); ++i)
            windows.get(i).setAgent(agents.get(i));
    }
    
    @Override
    public void tableChanged(Table table)
    {
        setTable(table);
    }
    
    @Override
    public void tableHasWinner(Table table, Agent agent)
    {
        overlay.setText(agent + " has won");
        overlay.setVisible(true);
    }
    
    private void initOverlay()
    {
        overlay = new JLabel();
        setGlassPane(overlay);
        
        overlay.setFont(overlay.getFont().deriveFont(40.0f));
        overlay.setOpaque(true);
        overlay.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
        overlay.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseReleased(MouseEvent e) {
                overlay.setVisible(false);
            }
        });
    }
    
